    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try {
            Reservations res = ReservationsDao.rechercherchereservationbyemailclient(clientscb.getSelectedItem().toString());
            ClientServices cs = new ClientServices(res.getReservation_id(), servicescb.getSelectedItem().toString(), "pending", LocalDate.now(), LocalDate.now(), desctf.getText().toString());
            ClientServices cs1 = ClientServicesDao.createClientServices(cs);
            if (cs1.getService_id() > 0) {

                JOptionPane.showMessageDialog(this, "Service en cours de traitement");
            } else {
                JOptionPane.showMessageDialog(this, "Une erreur est survenu, veuillez ressayer plus tard");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Service.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Service.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

