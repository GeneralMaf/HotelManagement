    private void getformulaire() {
        try {
            List<Reservations> listclientmail = ReservationsDao.getreservations();
            for (Reservations i : listclientmail) {
                clientscb.addItem(i.getClient_email());
            }
        } catch (SQLException ex) {
            Logger.getLogger(Service.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Service.class.getName()).log(Level.SEVERE, null, ex);
        }
        List<String> typeservices = List.of("housekeeping", "maintenance", "room_service");
        for (String i : typeservices) {
            servicescb.addItem(i);
        }
    }

