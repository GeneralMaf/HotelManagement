    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
      if(!roomnumbertf.getText().toString().isEmpty() && !roomtypetf.getText().toString().isEmpty() && !pricetf.getText().toString().isEmpty() && !dectf.getText().toString().isEmpty()){
      Rooms room=new Rooms(roomnumbertf.getText().toString(),roomtypetf.getText().toString(),roomstatuscb.getSelectedItem().toString(),Float.parseFloat(pricetf.getText().toString()), dectf.getText().toString());
          try {
              RoomsDao.createrooms(room);
              if(room.getRoom_id()>0){
                  JOptionPane.showMessageDialog(this,"Chambre ajoute");
                  this.setVisible(false);
                  new Chambre().setVisible(true);
              }else{
                  JOptionPane.showMessageDialog(this,"Une erreur est survenue");
              }
          } catch (SQLException ex) {
              Logger.getLogger(AddChambres.class.getName()).log(Level.SEVERE, null, ex);
          } catch (ClassNotFoundException ex) {
              Logger.getLogger(AddChambres.class.getName()).log(Level.SEVERE, null, ex);
          }
      }else{
       JOptionPane.showMessageDialog(this,"Veuillez remplir tous les champs");
      }
    }//GEN-LAST:event_jButton5ActionPerformed

