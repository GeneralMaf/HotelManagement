    private void getstatus() {
        List<String> mylist = List.of("available", "occupied", "to_clean");
        for (String i : mylist) {
            statuscb.addItem(i);
        }
    }

    private void getformuaire() {
        table.getSelectionModel().addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting()) {
                int selectedRow = table.getSelectedRow();
                if (selectedRow != -1) {

                    try {
                        List<Rooms> mylist = RoomsDao.selectrooms();
                        r = mylist.get(selectedRow);
                        if (r != null) {
                            roomnumbertf.setText(r.getRoom_number());
                            roomtypetf.setText(r.getRoom_type());
                            statuscb.setSelectedItem(r.getStatus());
                            pricetf.setText(String.valueOf(r.getPrice()));
                            dectf.setText(r.getDescription());

                        }

                    } catch (SQLException ex) {
                        Logger.getLogger(Reservation.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (ClassNotFoundException ex) {
                        Logger.getLogger(Reservation.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        });

    }

    private void gettable() {
        try {
            UtilsFonction.displayDataInTable(RoomsDao.selectrooms(), table, List.of("room_id"));
        } catch (SQLException ex) {
            Logger.getLogger(Chambre.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Chambre.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

