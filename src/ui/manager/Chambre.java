    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try {
            Rooms r1;

            if (!roomnumbertf.getText().toString().isEmpty() && !roomtypetf.getText().toString().isEmpty()  && !pricetf.getText().toString().isEmpty() && !dectf.getText().toString().isEmpty()) {
                r1 = new Rooms(r.getRoom_id(), roomnumbertf.getText().toString(), roomtypetf.getText().toString(), statuscb.getSelectedItem().toString(), Float.parseFloat(pricetf.getText().toString()), dectf.getText().toString());
                if (r1 != null) {
                    int row = RoomsDao.updateRomm(r1);

                    if (row > 0) {
                        JOptionPane.showMessageDialog(this, "Modification avec succes");
                    } else {
                        JOptionPane.showMessageDialog(this, "Une erreur est survenue");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Veuillez selectionner une ligne");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs d'abord");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Chambre.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Chambre.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

